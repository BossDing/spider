    /**
     * HttpClient调用执行
     * @param base
     * @return
     */
    private static String call(HttpClient httpClient, HttpRequestBase base) {
        try {
            HttpResponse response = httpClient.execute(base);
            StatusLine httpstatus = response.getStatusLine();
            if (httpstatus.getStatusCode() != HttpStatus.SC_OK) {
                logger.error("HttpClient：调用[" + base.getURI()+ "]失败,状态码[" + httpstatus.getStatusCode() + "]");
                return null;
            }
            HttpEntity resEntity = response.getEntity();
//            String charSet = cs != null ? cs.name() : "UTF-8"; // 获取字符编码
//            String relsult = EntityUtils.toString(resEntity, charSet);
//            EntityUtils.consume(resEntity); // 关闭流
            String result = EntityUtils.toString(resEntity);
            return result;
        } catch (Exception e) {
            logger.error("HttpClient：调用[" + base.getURI() + "]失败,原因["+ e.getLocalizedMessage() + "]");
            return null;
        } finally {
            if (httpClient != null) { // 关闭连接
                httpClient.getConnectionManager().shutdown();
            }
        }
    }

    private static String buildUrl(String host, String path, Map<String, String> querys) throws UnsupportedEncodingException {
        StringBuilder sbUrl = new StringBuilder();
        sbUrl.append(host);
        if (!StringUtils.isBlank(path)) {
            sbUrl.append(path);
        }
        if (null != querys) {
            StringBuilder sbQuery = new StringBuilder();
            for (Map.Entry<String, String> query : querys.entrySet()) {
                if (0 < sbQuery.length()) {
                    sbQuery.append("&");
                }
                if (StringUtils.isBlank(query.getKey()) && !StringUtils.isBlank(query.getValue())) {
                    sbQuery.append(query.getValue());
                }
                if (!StringUtils.isBlank(query.getKey())) {
                    sbQuery.append(query.getKey());
                    if (!StringUtils.isBlank(query.getValue())) {
                        sbQuery.append("=");
                        sbQuery.append(URLEncoder.encode(query.getValue(), "utf-8"));
                    }
                }
            }
            if (0 < sbQuery.length()) {
                sbUrl.append("?").append(sbQuery);
            }
        }

        return sbUrl.toString();
    }

    private static HttpClient wrapClient(String host) {
        HttpClient httpClient = new DefaultHttpClient();
        if (host.startsWith("https://")) {
            sslClient(httpClient);
        }

        return httpClient;
    }

    private static void sslClient(HttpClient httpClient) {
        try {
            SSLContext ctx = SSLContext.getInstance("TLS");
            X509TrustManager tm = new X509TrustManager() {
                public X509Certificate[] getAcceptedIssuers() {
                    return null;
                }
                public void checkClientTrusted(X509Certificate[] xcs, String str) {

                }
                public void checkServerTrusted(X509Certificate[] xcs, String str) {

                }
            };
            ctx.init(null, new TrustManager[] { tm }, null);
            SSLSocketFactory ssf = new SSLSocketFactory(ctx);
            ssf.setHostnameVerifier(SSLSocketFactory.ALLOW_ALL_HOSTNAME_VERIFIER);
            ClientConnectionManager ccm = httpClient.getConnectionManager();
            SchemeRegistry registry = ccm.getSchemeRegistry();
            registry.register(new Scheme("https", ssf, 443));
        } catch (KeyManagementException ex) {
            throw new RuntimeException(ex);
        } catch (NoSuchAlgorithmException ex) {
            throw new RuntimeException(ex);
        }
    }
    
